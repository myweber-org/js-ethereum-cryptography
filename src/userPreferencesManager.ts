const VALID_LANGUAGES = new Set(['en-US', 'es-ES', 'fr-FR', 'de-DE']);
const MIN_FONT_SIZE = 8;
const MAX_FONT_SIZE = 32;
const MIN_AUTO_SAVE_INTERVAL = 1000;
const MAX_AUTO_SAVE_INTERVAL = 300000;

class UserPreferencesManager {
  private preferences: UserPreferences;
  private storageKey: string;

  constructor(storageKey: string = 'userPreferences') {
    this.storageKey = storageKey;
    this.preferences = this.loadPreferences();
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(this.storageKey);
      if (stored) {
        const parsed = JSON.parse(stored);
        return this.validateAndMerge(parsed);
      }
    } catch (error) {
      console.warn('Failed to load preferences from storage:', error);
    }
    return { ...DEFAULT_PREFERENCES };
  }

  private validateAndMerge(partial: Partial<UserPreferences>): UserPreferences {
    const merged = { ...DEFAULT_PREFERENCES, ...partial };
    
    if (!['light', 'dark', 'auto'].includes(merged.theme)) {
      merged.theme = DEFAULT_PREFERENCES.theme;
    }
    
    if (!VALID_LANGUAGES.has(merged.language)) {
      merged.language = DEFAULT_PREFERENCES.language;
    }
    
    merged.fontSize = Math.max(MIN_FONT_SIZE, Math.min(MAX_FONT_SIZE, merged.fontSize));
    merged.autoSaveInterval = Math.max(
      MIN_AUTO_SAVE_INTERVAL,
      Math.min(MAX_AUTO_SAVE_INTERVAL, merged.autoSaveInterval)
    );
    
    return merged;
  }

  getPreferences(): UserPreferences {
    return { ...this.preferences };
  }

  updatePreferences(updates: Partial<UserPreferences>): boolean {
    const newPreferences = this.validateAndMerge({ ...this.preferences, ...updates });
    
    if (JSON.stringify(this.preferences) === JSON.stringify(newPreferences)) {
      return false;
    }
    
    this.preferences = newPreferences;
    this.persistPreferences();
    return true;
  }

  resetToDefaults(): boolean {
    if (JSON.stringify(this.preferences) === JSON.stringify(DEFAULT_PREFERENCES)) {
      return false;
    }
    
    this.preferences = { ...DEFAULT_PREFERENCES };
    this.persistPreferences();
    return true;
  }

  private persistPreferences(): void {
    try {
      localStorage.setItem(this.storageKey, JSON.stringify(this.preferences));
    } catch (error) {
      console.error('Failed to persist preferences:', error);
    }
  }

  subscribe(callback: (prefs: UserPreferences) => void): () => void {
    const storageListener = (event: StorageEvent) => {
      if (event.key === this.storageKey && event.newValue) {
        try {
          const newPrefs = JSON.parse(event.newValue);
          this.preferences = this.validateAndMerge(newPrefs);
          callback(this.getPreferences());
        } catch (error) {
          console.warn('Failed to parse updated preferences:', error);
        }
      }
    };

    window.addEventListener('storage', storageListener);
    
    return () => {
      window.removeEventListener('storage', storageListener);
    };
  }
}

