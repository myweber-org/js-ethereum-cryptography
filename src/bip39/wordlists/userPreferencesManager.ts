class UserPreferencesManager {
  private static readonly STORAGE_KEY = 'user_preferences';
  private preferences: UserPreferences;

  constructor(defaultPreferences?: Partial<UserPreferences>) {
    this.preferences = this.loadPreferences() || this.getDefaultPreferences();
    
    if (defaultPreferences) {
      this.updatePreferences(defaultPreferences);
    }
  }

  private getDefaultPreferences(): UserPreferences {
    return {
      theme: 'auto',
      language: 'en-US',
      notificationsEnabled: true,
      fontSize: 14
    };
  }

  private loadPreferences(): UserPreferences | null {
    try {
      const stored = localStorage.getItem(UserPreferencesManager.STORAGE_KEY);
      return stored ? JSON.parse(stored) : null;
    } catch {
      return null;
    }
  }

  private savePreferences(): void {
    try {
      localStorage.setItem(
        UserPreferencesManager.STORAGE_KEY,
        JSON.stringify(this.preferences)
      );
    } catch (error) {
      console.error('Failed to save preferences:', error);
    }
  }

  getPreferences(): UserPreferences {
    return { ...this.preferences };
  }

  updatePreferences(updates: Partial<UserPreferences>): boolean {
    const validated = this.validatePreferences(updates);
    
    if (validated) {
      this.preferences = { ...this.preferences, ...validated };
      this.savePreferences();
      return true;
    }
    
    return false;
  }

  private validatePreferences(updates: Partial<UserPreferences>): Partial<UserPreferences> | null {
    const validated: Partial<UserPreferences> = {};

    if (updates.theme !== undefined) {
      if (['light', 'dark', 'auto'].includes(updates.theme)) {
        validated.theme = updates.theme;
      } else {
        return null;
      }
    }

    if (updates.language !== undefined) {
      if (typeof updates.language === 'string' && updates.language.length >= 2) {
        validated.language = updates.language;
      } else {
        return null;
      }
    }

    if (updates.notificationsEnabled !== undefined) {
      if (typeof updates.notificationsEnabled === 'boolean') {
        validated.notificationsEnabled = updates.notificationsEnabled;
      } else {
        return null;
      }
    }

    if (updates.fontSize !== undefined) {
      if (typeof updates.fontSize === 'number' && updates.fontSize >= 8 && updates.fontSize <= 32) {
        validated.fontSize = updates.fontSize;
      } else {
        return null;
      }
    }

    return validated;
  }

  resetToDefaults(): void {
    this.preferences = this.getDefaultPreferences();
    this.savePreferences();
  }

  clearPreferences(): void {
    try {
      localStorage.removeItem(UserPreferencesManager.STORAGE_KEY);
      this.preferences = this.getDefaultPreferences();
    } catch (error) {
      console.error('Failed to clear preferences:', error);
    }
  }
}

