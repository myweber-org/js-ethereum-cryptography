const DEFAULT_PREFERENCES: UserPreferences = {
  theme: 'auto',
  fontSize: 16,
  notificationsEnabled: true,
  language: 'en-US'
};

const STORAGE_KEY = 'app_user_preferences';

class UserPreferencesManager {
  private preferences: UserPreferences;

  constructor() {
    this.preferences = this.loadPreferences();
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(STORAGE_KEY);
      if (stored) {
        const parsed = JSON.parse(stored);
        return { ...DEFAULT_PREFERENCES, ...parsed };
      }
    } catch (error) {
      console.warn('Failed to load preferences from localStorage:', error);
    }
    return { ...DEFAULT_PREFERENCES };
  }

  private savePreferences(): void {
    try {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(this.preferences));
    } catch (error) {
      console.warn('Failed to save preferences to localStorage:', error);
    }
  }

  getPreferences(): UserPreferences {
    return { ...this.preferences };
  }

  updatePreferences(updates: Partial<UserPreferences>): void {
    this.preferences = { ...this.preferences, ...updates };
    this.savePreferences();
  }

  resetToDefaults(): void {
    this.preferences = { ...DEFAULT_PREFERENCES };
    this.savePreferences();
  }

  getTheme(): UserPreferences['theme'] {
    return this.preferences.theme;
  }

  setTheme(theme: UserPreferences['theme']): void {
    this.updatePreferences({ theme });
  }

  getFontSize(): number {
    return this.preferences.fontSize;
  }

  setFontSize(size: number): void {
    if (size >= 12 && size <= 24) {
      this.updatePreferences({ fontSize: size });
    }
  }

  areNotificationsEnabled(): boolean {
    return this.preferences.notificationsEnabled;
  }

  toggleNotifications(): void {
    this.updatePreferences({
      notificationsEnabled: !this.preferences.notificationsEnabled
    });
  }

  getLanguage(): string {
    return this.preferences.language;
  }

  setLanguage(language: string): void {
    if (['en-US', 'es-ES', 'fr-FR', 'de-DE'].includes(language)) {
      this.updatePreferences({ language });
    }
  }
}

export const userPreferences = new UserPreferencesManager();