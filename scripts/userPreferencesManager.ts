class UserPreferencesManager {
  private readonly storageKey = 'user_preferences_v1';

  getPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(this.storageKey);
      if (stored) {
        const parsed = JSON.parse(stored);
        return { ...DEFAULT_PREFERENCES, ...parsed };
      }
    } catch (error) {
      console.warn('Failed to load user preferences:', error);
    }
    return { ...DEFAULT_PREFERENCES };
  }

  updatePreferences(updates: Partial<UserPreferences>): UserPreferences {
    const current = this.getPreferences();
    const updated = { ...current, ...updates };
    
    try {
      localStorage.setItem(this.storageKey, JSON.stringify(updated));
    } catch (error) {
      console.warn('Failed to save user preferences:', error);
    }
    
    return updated;
  }

  resetToDefaults(): UserPreferences {
    try {
      localStorage.removeItem(this.storageKey);
    } catch (error) {
      console.warn('Failed to reset user preferences:', error);
    }
    return { ...DEFAULT_PREFERENCES };
  }

  migrateFromLegacyStorage(): void {
    const legacyKeys = ['user_settings', 'app_preferences'];
    
    for (const key of legacyKeys) {
      try {
        const legacyData = localStorage.getItem(key);
        if (legacyData) {
          const parsed = JSON.parse(legacyData);
          this.updatePreferences(this.transformLegacyData(parsed));
          localStorage.removeItem(key);
        }
      } catch (error) {
        console.warn(`Failed to migrate legacy storage key "${key}":`, error);
      }
    }
  }

  private transformLegacyData(legacyData: any): Partial<UserPreferences> {
    const transformed: Partial<UserPreferences> = {};
    
    if (legacyData.theme && ['light', 'dark', 'auto'].includes(legacyData.theme)) {
      transformed.theme = legacyData.theme;
    }
    
    if (typeof legacyData.fontSize === 'number') {
      transformed.fontSize = Math.max(8, Math.min(32, legacyData.fontSize));
    }
    
    if (typeof legacyData.notifications === 'boolean') {
      transformed.notificationsEnabled = legacyData.notifications;
    }
    
    if (typeof legacyData.lang === 'string') {
      transformed.language = legacyData.lang;
    }
    
    return transformed;
  }
}

