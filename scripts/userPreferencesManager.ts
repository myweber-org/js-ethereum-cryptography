class UserPreferencesManager {
  private static readonly STORAGE_KEY = 'user_preferences';
  private preferences: UserPreferences;

  constructor(defaultPreferences?: Partial<UserPreferences>) {
    this.preferences = this.loadPreferences();
    if (defaultPreferences) {
      this.preferences = { ...this.preferences, ...defaultPreferences };
    }
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(UserPreferencesManager.STORAGE_KEY);
      if (stored) {
        return JSON.parse(stored);
      }
    } catch (error) {
      console.warn('Failed to load preferences from storage:', error);
    }
    
    return this.getDefaultPreferences();
  }

  private getDefaultPreferences(): UserPreferences {
    return {
      theme: 'auto',
      language: 'en-US',
      notificationsEnabled: true,
      fontSize: 16
    };
  }

  updatePreferences(updates: Partial<UserPreferences>): void {
    const oldPreferences = { ...this.preferences };
    this.preferences = { ...this.preferences, ...updates };
    
    try {
      localStorage.setItem(
        UserPreferencesManager.STORAGE_KEY, 
        JSON.stringify(this.preferences)
      );
      this.notifyPreferencesChange(oldPreferences, this.preferences);
    } catch (error) {
      console.error('Failed to save preferences:', error);
      throw new Error('Preferences save failed');
    }
  }

  getPreferences(): Readonly<UserPreferences> {
    return { ...this.preferences };
  }

  resetToDefaults(): void {
    this.updatePreferences(this.getDefaultPreferences());
  }

  validatePreferences(prefs: Partial<UserPreferences>): boolean {
    if (prefs.theme && !['light', 'dark', 'auto'].includes(prefs.theme)) {
      return false;
    }
    
    if (prefs.fontSize && (prefs.fontSize < 8 || prefs.fontSize > 32)) {
      return false;
    }
    
    if (prefs.language && typeof prefs.language !== 'string') {
      return false;
    }
    
    return true;
  }

  private notifyPreferencesChange(
    oldPrefs: UserPreferences, 
    newPrefs: UserPreferences
  ): void {
    const changes: string[] = [];
    
    if (oldPrefs.theme !== newPrefs.theme) {
      changes.push(`theme: ${oldPrefs.theme} -> ${newPrefs.theme}`);
    }
    
    if (oldPrefs.language !== newPrefs.language) {
      changes.push(`language: ${oldPrefs.language} -> ${newPrefs.language}`);
    }
    
    if (oldPrefs.fontSize !== newPrefs.fontSize) {
      changes.push(`fontSize: ${oldPrefs.fontSize} -> ${newPrefs.fontSize}`);
    }
    
    if (oldPrefs.notificationsEnabled !== newPrefs.notificationsEnabled) {
      changes.push(`notifications: ${oldPrefs.notificationsEnabled} -> ${newPrefs.notificationsEnabled}`);
    }
    
    if (changes.length > 0) {
      console.log('Preferences changed:', changes.join(', '));
    }
  }
}

